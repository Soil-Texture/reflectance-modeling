def elbow(y):
    d_start = abs(y[1] - y[0])
    n_end = 1
    while 1/abs((y[n_end] - y[n_end-1]))  > d_start and n_end < (len(y)-1):
        n_end += 1
    d_elbow = (y[0] - y[n_end])/n_end
    n_elbow = 1
    while y[n_elbow] - y[n_elbow+1] > d_elbow:
        n_elbow += 1
    return n_elbow

